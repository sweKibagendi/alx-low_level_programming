#include <stdio.h>

/**
 * main - Entry point
 * Description: Prints base 10 digits
 * Return: Always 0
 */

int main(void)
{
	/**
	 * Declare int n
	 * For loop
	 * Initialize n to 48
	 * Set limit to 57
	 * Increment n
	 * Print n
	 * Print newline
	 */

	int n;

	for (n = 48; n <= 57; n++)
		putchar(n);
	putchar('\n');
	return (0);
}
